from tkinter import*
myw=Tk()
myw.geometry('500x200')
myw.configure(bg='purple')
myw.title("example of frame")
leftframe=Frame(myw,height=100,width=200,bg='pink')
leftframe.place(x=10,y=10)
rightframe=Frame(myw,height=100,width=200,bg='cyan')
rightframe.place(x=220,y=10)
l1=Label(leftframe,text="label1")
l1.place(x=10,y=10)
l2=Label(rightframe,text="label2")
l2.place(x=10,y=10)
l3=Label(rightframe,text="label3")
l3.place(x=10,y=50)
mainloop()
